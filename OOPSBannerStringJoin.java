public class OOPSBannerStringJoin
{
	public static void main()
	{
		System.out.println(String.join("  *****  "," ","  *****  "," "," ******* "," "," ********"));
		System.out.println(String.join(" ******* "," "," ******* "," ","**     **"," ","**     **"));
		System.out.println(String.join("**     **"," ","**     **"," ","**     **"," ","**       "));
		System.out.println(String.join("**     **"," ","**     **"," ","******** "," "," ******* "));
		System.out.println(String.join("**     **"," ","**     **"," ","**       "," ","       **"));
		System.out.println(String.join(" ******* "," "," ******* "," ","**       "," ","**     **"));
		System.out.println(String.join("  *****  "," ","  *****  "," ","**       "," ","******** "));
	}
}
